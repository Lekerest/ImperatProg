#include <stdio.h>

int main(void)
{
    // ---------- ЧТЕНИЕ ВХОДНЫХ ДАННЫХ ----------
    // N — количество людей в круге
    // K — шаг: каждый K-й человек выбывает
    int N;
    int K;

    scanf("%d %d", &N, &K);

    // ---------- ОСНОВНАЯ ИДЕЯ (ЗАДАЧА ИОСИФА) ----------
    // Внутри цикла мы считаем ответ в 0-индексации:
    // люди имеют номера 0..i-1 для текущего размера круга i.
    //
    // result = позиция победителя среди i людей (в 0-индексации).
    // База: среди 1 человека победитель всегда 0.
    int result = 0;

    // ---------- ПОШАГОВОЕ ПОСТРОЕНИЕ ОТ 1 ДО N ----------
    // Мы увеличиваем размер круга i от 1 до N и каждый раз пересчитываем,
    // где будет победитель при добавлении очередного человека.
    for (int i = 1; i <= N; i++)
    {
        result = (result + K) % i;
    }

    // ---------- ПЕРЕВОД В НУМЕРАЦИЮ ПО УСЛОВИЮ ----------
    // По условию люди нумеруются 1..N, а result сейчас 0..N-1,
    // поэтому прибавляем 1.
    result = result + 1;

    // ---------- ВЫВОД ОТВЕТА ----------
    printf("%d\n", result);

    return 0;
}
