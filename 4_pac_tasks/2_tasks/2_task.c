#include <stdio.h>

// start — указатель на первый символ строки.
// len   — длина строки (без учёта '\0').
void reverse(char *start, int len)
{
    int i;
    char temp;

    // Проходим только первую половину строки.
    // Симметрично меняем местами символы:
    //   start[i]           — символ слева
    //   start[len-i-1]     — символ справа
    for (i = 0; i < len / 2; i++)
    {
        // Сохраняем левый символ
        temp = start[i];

        // Левую позицию заменяем правой
        start[i] = start[len - i - 1];

        // Правую позицию заменяем сохранённым левым символом
        start[len - i - 1] = temp;
    }
}


// Возвращает длину строки, не считая завершающий
// нулевой символ '\0'.
int my_strlen(const char *s)
{
    int len = 0;

    // Пока текущий символ не равен нулевому символу —
    // продолжаем считать длину
    while (s[len] != '\0')
    {
        len++; // Переходим к следующему символу
    }

    // Когда встретили '\0', возвращаем длину
    return len;
}

int main(void)
{
    // Открываем файлы: input.txt — для чтения, output.txt — для записи
    FILE *input = fopen("input.txt", "r");
    FILE *output = fopen("output.txt", "w");

    int N;
    fscanf(input, "%d", &N);   // Считываем количество строк

    // Буфер для строки. Максимальная длина строки — 100,
    // +1 символ для '\0'
    char buffer[101];


    // Обрабатываем N строк
    for (int i = 0; i < N; i++)
    {
        // Считываем строку (до пробела, т.к. используется %s)
        fscanf(input, "%s", buffer);

        // Определяем длину строки
        int len = my_strlen(buffer);

        // Разворачиваем строку
        reverse(buffer, len);

        // Записываем развёрнутую строку в выходной файл
        fprintf(output, "%s\n", buffer);
    }

    // Закрываем файлы
    fclose(input);
    fclose(output);

    return 0;
}